package trungtamjava;

public class Vehicle {
	private double price;
	private int seat;
	public double getPrice() {
		return price;
	}
	public void setPrice(double d) {
		this.price = d;
	}
	public int getSeat() {
		return seat;
	}
	public void setSeat(int seat) {
		this.seat = seat;
	}
	public Vehicle() {
		super();
		this.price = price;
		this.seat = seat;
	}
	

}
